function escollirOpcio() {
    
    controller.moveSprite(mySprite)
    animation.runImageAnimation(mySprite, assets.animation`
        myAnim0
    `, 500, true)
    info.startCountdown(20)
    if (solucions[num] == resposta && Respost == 1) {
        mySprite.sayText("CORRECT", 500, false)
        music.play(music.melodyPlayable(music.powerUp), music.PlaybackMode.UntilDone)
        info.changeScoreBy(1)
        Respost = 0
        info.startCountdown(20)
    } else if (solucions[num] != resposta && Respost == 1) {
        info.changeLifeBy(-1)
        Respost = 0
        mySprite.sayText("INCORRECT", 500, false)
        info.startCountdown(20)
    }
    
}

function professor() {
    
    if (info.life() > 0) {
        num = randint(0, 1)
        //  Display the text
        game.showLongText(preguntes[num], DialogLayout.Center)
        mySprite.sayText("Choose an option and press B", 1000, false)
        pause(1000)
        escollirOpcio()
    }
    
}

info.onCountdownEnd(function on_countdown_end() {
    info.changeLifeBy(-1)
    info.startCountdown(20)
    if (info.life() > 0) {
        professor()
    }
    
})
